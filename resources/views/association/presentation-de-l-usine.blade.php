<h1>Présentation de L'Usine</h1>
<p>
  L’Usine est un Centre Culturel Autogéré située dans le bâtiment de l’ancienne Usine Genevoise de Dégrossissage d’Or – UGDO- attribué par la Ville de Genève pour y réaliser des activités artistiques et socioculturelles.
</p>
<p>
  L’Usine est composée de 18 entités :
  <br>
  ArchiCouture – atelier de couture | AzzuroMatto – atelier de photographie | Le Cheveu sur la soupe – coiffeur | Coffre-fort – studio d’enregistrement | Compost – graphisme | Crache-Papier – atelier de sérigraphie | Forde – espace d’art contemporain | Kalvingrad – Concerts | Laika Films | La Makhno – Concerts et cafés politiques | Radio-Usine | Reklam – impression et promotion | SHBLTH – atelier d’artistes pluridisciplinaire | Le Spoutnik – Cinéma | Studio des Forces motrices – studio d’enregistrement | Théâtre de l’Usine – Arts de la scène | Urgence Disk | Le Zoo – salle de musiques électroniques.
</p>
<p>
  Le Bâtiment est également occupé par l’association Autrement Aujourd’hui (3ème étage), les ateliers d’artistes du Fond Municipal d’Art Contemporain – FMAC- (3ème et 4ème étage) et l’association PTR (partage de la salle du Rez avec l’association Kalvingrad).
</p>
